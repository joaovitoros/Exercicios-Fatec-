package dicionario;

public class Palavra {
	public String nome;
	public String definicao;
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getDefinicao() {
		return definicao;
	}
	
	public void setDefinicao(String definicao) {
		this.definicao = definicao;
	}
}
